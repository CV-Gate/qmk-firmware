#ifdef OLED_ENABLE
#include <stdio.h>

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    return OLED_ROTATION_270; // Rotate the display for proper vertical alignment
}

#define L_BASE 0
#define L_LOWER 2
#define L_RAISE 4
#define L_ADJUST 8

void oled_render_layer_state(void) {
    oled_write_P(PSTR("Layer: "), false);
    switch (layer_state) {
        case L_BASE:
            oled_write_ln_P(PSTR("Def"), false);
            break;
        case L_LOWER:
            oled_write_ln_P(PSTR("Low"), false);
            break;
        case L_RAISE:
            oled_write_ln_P(PSTR("Rai"), false);
            break;
        case L_ADJUST:
        case L_ADJUST|L_LOWER:
        case L_ADJUST|L_RAISE:
        case L_ADJUST|L_LOWER|L_RAISE:
            oled_write_ln_P(PSTR("Adj"), false);
            break;
    }
}


void oled_render_wpm(void) {
    char wpm_str[8];
    snprintf(wpm_str, sizeof(wpm_str), "WPM: %03d", get_current_wpm());
    oled_write_ln(wpm_str, false);
}

bool oled_task_user(void) {
    oled_render_layer_state();
    oled_render_wpm();
    return false; // Return false to indicate that no further processing is needed
}

#endif // OLED_ENABLE
